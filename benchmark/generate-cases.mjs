// Regenerates the benchmark fixtures under ./cases. Run: node benchmark/generate-cases.mjs
//
// Each fixture targets one path through flawless/arrow-return-style so the
// benchmark can attribute cost to a specific code path. See ./config.ts.

import { mkdirSync, writeFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const here = path.dirname(fileURLToPath(import.meta.url));
const casesDir = path.join(here, "cases");
mkdirSync(casesDir, { recursive: true });

/** A call expression long enough that the whole arrow line exceeds maxLen (80). */
function longCall(suffix = "") {
	return `callFunc(alphaArgument${suffix}, betaArgument${suffix}, gammaArgument${suffix}, deltaArgument${suffix}, epsilonArgument${suffix})`;
}

function header(description) {
	return `// GENERATED by benchmark/generate-cases.mjs — do not edit by hand.\n// ${description}\n\n`;
}

function emit(name, description, body) {
	writeFileSync(path.join(casesDir, name), header(description) + body + "\n");
}

const N_LIGHT = 60;
const N_WORKER = 40;

// 1. Short implicit arrows that already satisfy the rule. Pure AST math, the
//    worker is never consulted and nothing is reported.
emit(
	"no-violation-implicit.ts",
	"Short valid implicit-return arrows. No report, no worker consult.",
	Array.from({ length: N_LIGHT }, (_, i) => `const nv${i} = () => value${i} + ${i};`).join("\n"),
);

// 2. Single-return block bodies that collapse to implicit returns. Exercises the
//    fixer (report + fix loop) on the block->implicit path; never touches the
//    worker.
emit(
	"block-to-implicit.ts",
	"Single-return blocks collapsed to implicit returns. Fixer path, no worker.",
	Array.from({ length: N_LIGHT }, (_, i) => `const bi${i} = () => { return value${i} + ${i}; };`).join(
		"\n",
	),
);

// 3. Over-limit implicit arrows with DISTINCT statements: every consult is a
//    cache miss, so the per-file format cache never helps. This is the worst
//    case for the worker path. Benchmarked with useOxfmt:false vs true to
//    isolate the formatter/worker cost from the pure line-length math.
emit(
	"over-limit-distinct.ts",
	"Over-limit implicit arrows, all distinct. Cache-miss worker consults.",
	Array.from({ length: N_WORKER }, (_, i) => `const ol${i} = () => ${longCall(i)};`).join("\n"),
);

// 4. Over-limit implicit arrows that are TEXTUALLY IDENTICAL. Bare blocks give
//    each arrow the same statementOf() text and arrowIndex 0, so only the first
//    consult reaches the worker and the rest are served from the cache. Legal
//    to repeat `const x` because each block is its own scope. Compare against
//    case 3 to read cache effectiveness.
emit(
	"over-limit-repeated.ts",
	"Over-limit arrows, textually identical. Only the first consult hits the worker.",
	Array.from({ length: N_WORKER }, () => `{ const x = () => ${longCall()}; }`).join("\n"),
);

// 5. A larger, realistic-shaped file: mostly valid short arrows with a scatter
//    of block->implicit fixes and a few over-limit consults, approximating a
//    real module the rule would lint on save.
const realistic = [];
for (let i = 0; i < 120; i += 1) {
	if (i % 20 === 0) {
		realistic.push(`const r${i} = () => ${longCall(i)};`);
	} else if (i % 5 === 0) {
		realistic.push(`const r${i} = () => { return value${i} + ${i}; };`);
	} else {
		realistic.push(`const r${i} = () => value${i} + ${i};`);
	}
}

emit(
	"realistic.ts",
	"Large mixed file: mostly valid, some block->implicit, a few over-limit consults.",
	realistic.join("\n"),
);

console.info(`Wrote fixtures to ${casesDir}`);
